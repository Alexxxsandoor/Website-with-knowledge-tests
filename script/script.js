		infoDetailTheme.innerHTML = `<h3> ${course[courseIdTest].themes[i].info}</h3 ><p>${course[courseIdTest].themes[i].bigInfo}</p> <br> <a href=${course[courseIdTest].themes[i].video}>Видео на курс</a>`
